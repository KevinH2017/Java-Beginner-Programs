public class ForLoops {
    public static void main(String[] args) {
        for (int i=0; i<5; i++) {
            System.out.println("The value of i is: " + i);
        }
        // Formats string with integer and displays with newline
        for (int j=0; j<5; j++) {
            System.out.printf("The value of j is: %d\n", j); 
        }
    }
}